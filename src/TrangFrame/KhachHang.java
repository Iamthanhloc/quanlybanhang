public void checknull() {
        try {

            //check null
            if (txtmakh.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống !");
                txtmakh.requestFocus();
                return;
            }
            if (txttenkh.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Tên khách hàng không được để trống");
                txttenkh.requestFocus();
                return;
            }
            if (txtdiachi.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống!");
                txtdiachi.requestFocus();
                return;
            }

            if (txtSDT.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Nhập vào số điện thoại");
                txtSDT.requestFocus();
                return;
            } else if (txtSDT.getText().length() < 10 || txtSDT.getText().length() > 10) {
                JOptionPane.showMessageDialog(this, "Số điện thoại phải có 10 số");
                txtSDT.requestFocus();
                return;
            }
            if (txtNgaysinh.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập ngày sinh");
                txtNgaysinh.requestFocus();
                return;
            }

        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Lỗi nhập thông tin!");
        }
    }
